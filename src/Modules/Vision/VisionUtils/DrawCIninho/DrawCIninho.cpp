DrawCIninho::DrawCIninho(const QPointF& origin, qreal scale, qreal angle, const QColor& color) :
    m_origin(origin),
    m_scale(scale),
    m_angle(angle),
    m_color(color) {
}

void DrawCIninho::run(Painter2DBase* f) {
  for (auto polygon : CIninhoBody) {
    std::for_each(polygon.begin(), polygon.end(), [this](QPointF& point) {
      point = Geometry2D::rotateCCW(point, m_angle);
      point *= m_scale;
      point += m_origin;
    });
    f->drawFilledPolygon(polygon, m_color);
  }

  for (auto polygon : CIninhoEyes) {
    std::for_each(polygon.begin(), polygon.end(), [this](QPointF& point) {
      point = Geometry2D::rotateCCW(point, m_angle);
      point *= m_scale;
      point += m_origin;
    });
    f->drawFilledPolygon(polygon, f->backgroundColor());
  }

  for (auto polygon : CIninhoCircuit) {
    std::for_each(polygon.begin(), polygon.end(), [this](QPointF& point) {
      point = Geometry2D::rotateCCW(point, m_angle);
      point *= m_scale;
      point += m_origin;
    });
    f->drawFilledPolygon(polygon, f->backgroundColor());
  }
}

